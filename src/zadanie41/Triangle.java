package zadanie41;

public class Triangle {
    private int a;
    private int b;
    private int c;

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }

    public double getC() {
        return c;
    }

    protected Triangle(int a, int b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

}

